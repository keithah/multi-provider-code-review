  /**
   * Get Chart.js JavaScript
   * Note: JSON.stringify() provides XSS protection by properly escaping strings for JavaScript context
   */
  private getChartJS(costTrends: CostTrend[], perfTrends: PerformanceTrend[], stats: MetricsData): string {
    const recent = stats.reviews.slice(-100);
    // Placeholder values - severity breakdown not currently tracked in ReviewMetric
    const critical = 0;
    const major = 0;
    const minor = 0;

    // Placeholder values - provider success/failure not currently tracked per review
    const totalSuccess = recent.reduce((sum: number, r: ReviewMetric) => sum + r.providersUsed, 0);
    const totalFailed = 0;

    return `
      // Cost Trend Chart
      new Chart(document.getElementById('costTrendChart'), {
        type: 'line',
        data: {
          labels: ${JSON.stringify(costTrends.map((t) => t.date))},
          datasets: [{
            label: 'Daily Cost (USD)',
            data: ${JSON.stringify(costTrends.map((t) => t.cost))},
            borderColor: '#667eea',
            backgroundColor: 'rgba(102, 126, 234, 0.1)',
            tension: 0.4,
            fill: true
          }]
        },
        options: {
          responsive: true,
          plugins: { legend: { display: false } },
          scales: { y: { beginAtZero: true, ticks: { callback: value => '$' + value.toFixed(2) } } }
        }
      });

      // Performance Trend Chart
      new Chart(document.getElementById('perfTrendChart'), {
        type: 'line',
        data: {
          labels: ${JSON.stringify(perfTrends.map((t) => t.date))},
          datasets: [{
            label: 'Avg Duration (seconds)',
            data: ${JSON.stringify(perfTrends.map((t) => t.avgDuration))},
            borderColor: '#f5576c',
            backgroundColor: 'rgba(245, 87, 108, 0.1)',
            tension: 0.4,
            fill: true
          }]
        },
        options: {
          responsive: true,
          plugins: { legend: { display: false } },
          scales: { y: { beginAtZero: true, ticks: { callback: value => value + 's' } } }
        }
      });

      // Findings Distribution Chart
      new Chart(document.getElementById('findingsChart'), {
        type: 'doughnut',
        data: {
          labels: ['Critical', 'Major', 'Minor'],
          datasets: [{
            data: [${critical}, ${major}, ${minor}],
            backgroundColor: ['#e74c3c', '#f39c12', '#3498db']
          }]
        },
        options: { responsive: true, plugins: { legend: { position: 'bottom' } } }
      });

      // Provider Success Chart
      new Chart(document.getElementById('providerChart'), {
        type: 'pie',
        data: {
          labels: ['Success', 'Failed'],
          datasets: [{
            data: [${totalSuccess}, ${totalFailed}],
            backgroundColor: ['#2ecc71', '#e74c3c']
          }]
        },
        options: { responsive: true, plugins: { legend: { position: 'bottom' } } }
      });
    `;
  }
